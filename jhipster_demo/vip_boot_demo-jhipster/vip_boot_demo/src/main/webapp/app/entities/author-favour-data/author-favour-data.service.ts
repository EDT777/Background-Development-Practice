import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IAuthorFavourData } from '@/shared/model/author-favour-data.model';

const baseApiUrl = 'api/author-favour-data';

export default class AuthorFavourDataService {
  public find(id: number): Promise<IAuthorFavourData> {
    return new Promise<IAuthorFavourData>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IAuthorFavourData): Promise<IAuthorFavourData> {
    return new Promise<IAuthorFavourData>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IAuthorFavourData): Promise<IAuthorFavourData> {
    return new Promise<IAuthorFavourData>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
