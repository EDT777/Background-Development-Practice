import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IRelativeGoodsItem } from '@/shared/model/relative-goods-item.model';

const baseApiUrl = 'api/relative-goods-items';

export default class RelativeGoodsItemService {
  public find(id: number): Promise<IRelativeGoodsItem> {
    return new Promise<IRelativeGoodsItem>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IRelativeGoodsItem): Promise<IRelativeGoodsItem> {
    return new Promise<IRelativeGoodsItem>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IRelativeGoodsItem): Promise<IRelativeGoodsItem> {
    return new Promise<IRelativeGoodsItem>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
